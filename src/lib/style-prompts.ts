export function getStyleList() {
  return Object.entries(STYLE_CONFIG).map(([id, cfg]) => ({ id: id as StyleId, ...cfg }))
}

export function pickPrompt(styleId: StyleId, cursor = 0) {
  const cfg = STYLE_CONFIG[styleId]
  if (!cfg) throw new Error('鏃犳晥椋庢牸')
  const total = cfg.prompts.length
  const safeCursor = Math.max(0, cursor % total)
  // 杞崲 + 杞婚殢鏈烘壈鍔紙0~2锛?  const jitter = Math.floor(Math.random() * 3)
  const index = (safeCursor + jitter) % total
  const nextCursor = (index + 1) % total
  return {
    styleName: cfg.name,
    prompt: cfg.prompts[index],
    promptIndex: index,
    nextCursor,
    isFloorPlan: !!cfg.isFloorPlan,
  }
}
